class MedicalCellDatasetSafe(Dataset):
    """Dataset for training with safe aspect-ratio resizing to prevent OOM."""
    def __init__(self, root_dir, split='train', max_dim=1333):
        self.root_dir = root_dir
        self.split = split
        self.max_dim = max_dim
        self.split_dir = os.path.join(root_dir, 'train')
        self.items = [d for d in os.listdir(self.split_dir) if os.path.isdir(os.path.join(self.split_dir, d))]

    def __len__(self):
        return len(self.items)

    def __getitem__(self, idx):
        folder_name = self.items[idx]
        img_folder = os.path.join(self.split_dir, folder_name)
        img_path = os.path.join(img_folder, 'image.tif')

        img = cv2.imread(img_path, cv2.IMREAD_COLOR)
        h, w = img.shape[:2]

        scale = 1.0
        if max(h, w) > self.max_dim:
            scale = self.max_dim / float(max(h, w))
            new_w, new_h = int(w * scale), int(h * scale)
            img = cv2.resize(img, (new_w, new_h), interpolation=cv2.INTER_LINEAR)

        img = cv2.cvtColor(img, cv2.COLOR_BGR2RGB)
        img = F.to_tensor(img).float()

        masks, labels, boxes = [], [], []
        for class_id in range(1, 5):
            mask_path = os.path.join(img_folder, f'class{class_id}.tif')
            if os.path.exists(mask_path):
                class_mask = cv2.imread(mask_path, cv2.IMREAD_UNCHANGED)
                if scale != 1.0:
                    class_mask = cv2.resize(class_mask, (new_w, new_h), interpolation=cv2.INTER_NEAREST)

                obj_ids = np.unique(class_mask)
                obj_ids = obj_ids[obj_ids != 0]

                for obj_id in obj_ids:
                    instance_mask = (class_mask == obj_id)
                    pos = np.where(instance_mask)
                    if len(pos[0]) == 0:
                        continue

                    xmin, xmax = np.min(pos[1]), np.max(pos[1])
                    ymin, ymax = np.min(pos[0]), np.max(pos[0])
                    if xmin == xmax or ymin == ymax:
                        continue

                    masks.append(instance_mask)
                    boxes.append([xmin, ymin, xmax, ymax])
                    labels.append(class_id)

        target = {}
        if len(boxes) > 0:
            target["boxes"] = torch.as_tensor(boxes, dtype=torch.float32)
            target["labels"] = torch.as_tensor(labels, dtype=torch.int64)
            target["masks"] = torch.as_tensor(np.array(masks), dtype=torch.uint8)
        else:
            target["boxes"] = torch.zeros((0, 4), dtype=torch.float32)
            target["labels"] = torch.zeros((0,), dtype=torch.int64)
            target["masks"] = torch.zeros((0, img.shape[1], img.shape[2]), dtype=torch.uint8)

        return img, target


class MedicalCellTestDataset(Dataset):
    """Dataset for CodaBench inference."""
    def __init__(self, root_dir):
        self.split_dir = os.path.join(root_dir, 'test_release')
        self.items = [f for f in os.listdir(self.split_dir) if f.endswith('.tif')]

    def __len__(self):
        return len(self.items)

    def __getitem__(self, idx):
        file_name = self.items[idx]
        img_path = os.path.join(self.split_dir, file_name)
        img = cv2.imread(img_path, cv2.IMREAD_COLOR)
        img = cv2.cvtColor(img, cv2.COLOR_BGR2RGB)
        img = F.to_tensor(img).float()
        return img, file_name


def collate_fn(batch):
    return tuple(zip(*batch))

def encode_mask(binary_mask):
    arr = np.asfortranarray(binary_mask).astype(np.uint8)
    rle = mask_utils.encode(arr)
    if isinstance(rle['counts'], bytes):
        rle['counts'] = rle['counts'].decode('utf-8')
    return rle


