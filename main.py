def run_additional_experiments(model, train_dataset):
    print("Running Additional Experiments for Report...")
    eval_loader = DataLoader(train_dataset, batch_size=1, shuffle=True, num_workers=0, collate_fn=collate_fn)
    model.eval()

    all_images, all_targets, all_outputs = [], [], []

    with torch.no_grad():
        for i, (images, targets) in enumerate(eval_loader):
            if i >= 15:
                break
            images = list(img.to(device) for img in images)
            targets = [{k: v.to(device) for k, v in t.items()} for t in targets]
            outputs = model(images)

            all_images.append(images[0].cpu())
            all_targets.append({k: v.cpu() for k, v in targets[0].items()})
            all_outputs.append({k: v.cpu() for k, v in outputs[0].items()})

    # --- 1. Threshold Ablation Curve ---
    print("1/3 Generating Threshold Impact Curve...")
    thresholds = [0.05, 0.20, 0.35, 0.50, 0.65, 0.80, 0.95]
    avg_preds = [sum(np.sum((out['scores'].numpy() > t)) for out in all_outputs) / len(all_outputs) for t in thresholds]

    plt.figure(figsize=(8, 5))
    plt.plot(thresholds, avg_preds, marker='o', color='b', linewidth=2)
    plt.axvline(x=0.50, color='r', linestyle='--', label='0.50 Analysis Threshold')
    plt.title('Impact of Confidence Threshold on False Positives')
    plt.xlabel('Confidence Threshold')
    plt.ylabel('Average Predictions per Image')
    plt.grid(True, linestyle=':', alpha=0.7)
    plt.legend()
    plt.tight_layout()
    plt.savefig(os.path.join(OUTPUT_DIR, 'Exp1_Threshold.png'), dpi=300)
    plt.close()

    # --- 2. IoU Confusion Matrix ---
    print("2/3 Generating IoU Confusion Matrix...")
    conf_matrix = np.zeros((5, 5), dtype=np.int32)
    iou_thresh = 0.50

    for target, output in zip(all_targets, all_outputs):
        true_boxes, true_labels = target['boxes'], target['labels']
        keep = output['scores'] > 0.50
        pred_boxes, pred_labels = output['boxes'][keep], output['labels'][keep]

        if len(true_boxes) == 0 or len(pred_boxes) == 0:
            for tl in true_labels: conf_matrix[tl.item(), 0] += 1
            for pl in pred_labels: conf_matrix[0, pl.item()] += 1
            continue

        ious = box_iou(true_boxes, pred_boxes)
        matched = set()

        for true_idx, true_lbl in enumerate(true_labels):
            best_iou, best_pred = 0, -1
            for pred_idx, pred_lbl in enumerate(pred_labels):
                if pred_idx in matched: continue
                if ious[true_idx, pred_idx] > best_iou:
                    best_iou, best_pred = ious[true_idx, pred_idx], pred_idx

            if best_iou >= iou_thresh:
                matched.add(best_pred)
                conf_matrix[true_lbl.item(), pred_labels[best_pred].item()] += 1
            else:
                conf_matrix[true_lbl.item(), 0] += 1

        for pred_idx, pred_lbl in enumerate(pred_labels):
            if pred_idx not in matched:
                conf_matrix[0, pred_lbl.item()] += 1

    plt.figure(figsize=(8, 6))
    class_names = ['Background', 'Class 1', 'Class 2', 'Class 3', 'Class 4']
    sns.heatmap(conf_matrix, annot=True, fmt='d', cmap='Blues', xticklabels=class_names, yticklabels=class_names)
    plt.ylabel('True Target Class')
    plt.xlabel('Model Predicted Class')
    plt.title('Instance Segmentation Confusion Matrix (IoU > 0.5)')
    plt.tight_layout()
    plt.savefig(os.path.join(OUTPUT_DIR, 'Exp2_Matrix.png'), dpi=300)
    plt.close()

    # --- 3. Visual Ground Truth vs. Prediction ---
    print("3/3 Generating Visual Comparison...")
    img_np = np.clip(all_images[0].permute(1, 2, 0).numpy(), 0, 1)
    target, out = all_targets[0], all_outputs[0]

    fig, axes = plt.subplots(1, 2, figsize=(14, 7))
    colors = {1: 'red', 2: 'green', 3: 'blue', 4: 'yellow'}

    axes[0].imshow(img_np)
    axes[0].set_title("Ground Truth")
    axes[0].axis('off')
    for box, label in zip(target['boxes'].numpy(), target['labels'].numpy()):
        axes[0].add_patch(patches.Rectangle((box[0], box[1]), box[2]-box[0], box[3]-box[1],
                          linewidth=2, edgecolor=colors.get(label, 'white'), facecolor='none'))

    axes[1].imshow(img_np)
    axes[1].set_title("Predictions (>50% Confidence)")
    axes[1].axis('off')
    for box, label, score in zip(out['boxes'].numpy(), out['labels'].numpy(), out['scores'].numpy()):
        if score > 0.50:
            axes[1].add_patch(patches.Rectangle((box[0], box[1]), box[2]-box[0], box[3]-box[1],
                              linewidth=2, edgecolor=colors.get(label, 'white'), facecolor='none'))

    plt.tight_layout()
    plt.savefig(os.path.join(OUTPUT_DIR, 'Exp3_Visual.png'), dpi=300)
    plt.close()
    print("All experiments saved to outputs directory.")


