def generate_submission(model, test_dataset):
    print("Generating CodaBench Submission...")
    mapping_file_path = os.path.join(DATA_DIR, 'test_image_name_to_ids.json')
    with open(mapping_file_path, 'r') as f:
        mapping_data = json.load(f)

    name_to_id = {os.path.splitext(item['file_name'])[0]: int(item['id']) for item in mapping_data}

    submission_results = []
    model.eval()
    with torch.no_grad():
        for i in range(len(test_dataset)):
            img, file_name = test_dataset[i]
            base_name = os.path.splitext(file_name)[0]
            img_id = name_to_id.get(base_name)
            if img_id is None:
                continue

            out = model([img.to(device)])[0]

            boxes = out['boxes'].cpu().numpy()
            scores = out['scores'].cpu().numpy()
            labels = out['labels'].cpu().numpy()
            masks = out['masks'].cpu().numpy()

            for j in range(len(scores)):
                # Retaining the 0.05 threshold to maximize COCO mAP recall
                if scores[j] > 0.05:
                    mask_binary = (masks[j, 0] > 0.5).astype(np.uint8)
                    rle_segmentation = encode_mask(mask_binary)

                    xmin, ymin, xmax, ymax = boxes[j]
                    width = max(float(xmax - xmin), 1.0)
                    height = max(float(ymax - ymin), 1.0)

                    pred_dict = {
                        "image_id": int(img_id),
                        "category_id": int(labels[j]),
                        "bbox": [float(xmin), float(ymin), float(width), float(height)],
                        "score": float(scores[j]),
                        "segmentation": rle_segmentation
                    }
                    submission_results.append(pred_dict)

    json_filename = os.path.join(OUTPUT_DIR, 'test-results.json')
    zip_filename = os.path.join(OUTPUT_DIR, 'DL_HW3_Submission.zip')

    with open(json_filename, 'w') as f:
        json.dump(submission_results, f)
    with zipfile.ZipFile(zip_filename, 'w', zipfile.ZIP_DEFLATED) as zipf:
        # Zip must contain 'test-results.json' exactly, without folder paths
        zipf.write(json_filename, arcname='test-results.json')
    print(f"DONE! Submission saved as: {zip_filename}")


